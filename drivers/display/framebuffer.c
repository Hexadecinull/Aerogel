#define VBE_INFO_ADDR 0x6000
#define VBE_FLAG_ADDR 0x5FFC

static fb_info_t fb;

bool fb_init(void) {
    volatile u32 *flag = (volatile u32 *)VBE_FLAG_ADDR;
    if (*flag != 1) { fb.active = false; return false; }

    u8 *info    = (u8 *)VBE_INFO_ADDR;
    fb.pitch    = *(u16 *)(info + 0x10);
    fb.width    = *(u16 *)(info + 0x12);
    fb.height   = *(u16 *)(info + 0x14);
    fb.bpp      = *(u8  *)(info + 0x19);
    fb.addr     = *(u32 *)(info + 0x28);
    fb.active   = (fb.width > 0 && fb.height > 0 && fb.addr > 0);
    return fb.active;
}

bool fb_active(void) { return fb.active; }
u16  fb_width(void)  { return fb.width;  }
u16  fb_height(void) { return fb.height; }

static void put_pixel_32(int x, int y, u32 c) {
    if ((u16)x >= fb.width || (u16)y >= fb.height) return;
    u32 *p = (u32 *)(uptr)(fb.addr + (u32)y * fb.pitch + (u32)x * 4);
    *p = c;
}

static void put_pixel_24(int x, int y, u32 c) {
    if ((u16)x >= fb.width || (u16)y >= fb.height) return;
    u8 *p = (u8 *)(uptr)(fb.addr + (u32)y * fb.pitch + (u32)x * 3);
    p[0] = (u8)(c & 0xFF);
    p[1] = (u8)((c >> 8)  & 0xFF);
    p[2] = (u8)((c >> 16) & 0xFF);
}

void fb_pixel(int x, int y, u32 c) {
    if (fb.bpp == 32) put_pixel_32(x, y, c);
    else               put_pixel_24(x, y, c);
}

void fb_hline(int x, int y, int len, u32 c) {
    for (int i = 0; i < len; i++) fb_pixel(x + i, y, c);
}

void fb_vline(int x, int y, int len, u32 c) {
    for (int i = 0; i < len; i++) fb_pixel(x, y + i, c);
}

void fb_clear(u32 c) {
    if (fb.bpp == 32 && fb.pitch == (u32)fb.width * 4) {
        u32 *p = (u32 *)(uptr)fb.addr;
        u32  n = (u32)fb.width * fb.height;
        for (u32 i = 0; i < n; i++) p[i] = c;
    } else {
        for (int y = 0; y < fb.height; y++) fb_hline(0, y, fb.width, c);
    }
}

void fb_rect(int x, int y, int w, int h, u32 c) {
    for (int row = y; row < y + h; row++) fb_hline(x, row, w, c);
}

void fb_rect_outline(int x, int y, int w, int h, int t, u32 c) {
    fb_rect(x, y, w, t, c);
    fb_rect(x, y + h - t, w, t, c);
    fb_rect(x, y, t, h, c);
    fb_rect(x + w - t, y, t, h, c);
}

void fb_line(int x0, int y0, int x1, int y1, u32 c) {
    int dx = x1 - x0, dy = y1 - y0;
    int ax = dx < 0 ? -dx : dx, ay = dy < 0 ? -dy : dy;
    int sx = dx < 0 ? -1 : 1,  sy = dy < 0 ? -1 : 1;
    int err = (ax > ay ? ax : -ay) / 2;
    for (;;) {
        fb_pixel(x0, y0, c);
        if (x0 == x1 && y0 == y1) break;
        int e2 = err;
        if (e2 > -ax) { err -= ay; x0 += sx; }
        if (e2 <  ay) { err += ax; y0 += sy; }
    }
}

void fb_char(int x, int y, char ch, u32 fg, u32 bg) {
    u8 idx = (u8)ch;
    if (idx >= 128) idx = '?';
    const u8 *glyph = font_8x16[idx];
    for (int row = 0; row < FB_FONT_H; row++) {
        u8 bits = glyph[row];
        for (int col = 0; col < FB_FONT_W; col++) {
            u32 color = (bits & (0x80 >> col)) ? fg : bg;
            fb_pixel(x + col, y + row, color);
        }
    }
}

void fb_string(int x, int y, const char *s, u32 fg, u32 bg) {
    while (*s) {
        fb_char(x, y, *s++, fg, bg);
        x += FB_FONT_W;
    }
}
